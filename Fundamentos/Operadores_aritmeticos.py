produto_1 = 20
produto_2 = 10

print (produto_1 + produto_2)  # Posso colocar quantas variáveis eu precisar e quiser
print (produto_1 - produto_2)
print (produto_1 * produto_2)
print (produto_1 ** produto_2) # Exponenciação, na qual o valor após o * é o que se eleva Ex: p1 elevado a p2
print (produto_1 / produto_2)  # Divisor único -> Retorna valor FLOAT
print (produto_1 // produto_2) # Divisor duplo -> Retorna valor INT
print (produto_1 % produto_2)  # Módulo ou resto da divisão

x = 10 + 5 * 4  # A sequência de execução será de -> Parenteses / Expoentes / Multiplicação Divisão / Adição Sub
print(x)

y = (2 + 2) * (3 + 3) * (4 + 4) / 2 + (5 ** 5)
print(y)