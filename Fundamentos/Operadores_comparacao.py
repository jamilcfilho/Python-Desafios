saldo = 200
saque = 200

print(saldo == saque) # É igual
print(saldo != saque) # É diferente
print(saldo > saque)  # É maior
print(saldo >= saque) # É maior ou igual
print(saldo < saque)  # É menor
print(saldo <= saque) # É menor ou igual

