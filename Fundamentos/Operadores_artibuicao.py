saldo = 500     # Operador '=' sozinho se refere a atribuir valor para uma variável
print(saldo)

saldo = 200     # Mesma variável, porém as da linha inferior subscrevem as linhas de cima
print(saldo)

saldo += 10     # Operador '+=' é adição -> Saldo = Saldo + 10 e assim com os demais operadores
print(saldo)

saldo -= 5
print(saldo)

saldo //= 2
print(saldo)

saldo /= 2
print(saldo)

saldo *= 10
print(saldo)

saldo %= 4
print(saldo)

saldo **= 2
print(saldo)
