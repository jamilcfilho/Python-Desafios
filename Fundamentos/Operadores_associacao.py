# Operadores utilizados para verificar se um objeto esta presente em uma sequencia

# IN = se objeto ESTA na sequencia
# NOT IN = se objeto NÃO ESTA na sequencia

frutas = ["limao", "uva"]
curso = "Curso de python"    # Linguagem Python é 'Sensitive Case'

print("laranja" not in frutas)
print("limao" in frutas)
print("Python" in curso)